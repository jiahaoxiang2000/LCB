int crypto_hash(unsigned char *out, const unsigned char *in,
		unsigned long long inlen) {

	u32 dataFormat[4] = { 0 };
	// initialization
	u32 s[16] = { 0 };
	u8 tempData[32];
	//absorb

	while (inlen >= hash_RATE) {
		packU64FormatToFourPacket(dataFormat, in);
		s[0] ^= dataFormat[0];
		s[1] ^= dataFormat[1];
		s[2] ^= dataFormat[2];
		s[3] ^= dataFormat[3];
		P512(s, constant8Format_hash, PRH_ROUNDS);
		inlen -= hash_RATE;
		in += hash_RATE;
	}
	memset(tempData, 0, sizeof(tempData));

	memcpy(tempData, in, inlen * sizeof(unsigned char));
	tempData[inlen] = 0x01;
	packU64FormatToFourPacket(dataFormat, tempData);
	s[0] ^= dataFormat[0];
	s[1] ^= dataFormat[1];
	s[2] ^= dataFormat[2];
	s[3] ^= dataFormat[3];

	P512(s, constant8Format_hash, PRH_ROUNDS);
	//sequeez

	unpackU128FormatToFourPacket(out, s);
	unpackU128FormatToFourPacket((out + 16), (s + 4));

	P512(s, constant8Format_hash, PRH_ROUNDS);
	out += CRYPTO_BYTES / 2;
	unpackU128FormatToFourPacket(out, s);
	unpackU128FormatToFourPacket((out + 16), (s + 4));
	return 0;
}

